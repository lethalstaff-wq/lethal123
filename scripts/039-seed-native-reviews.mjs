// Materialize reviews from data/personas.json into Supabase.
// Reads the plan + data/style-vocab.json, composes text per review using
// country/rating/length templates, generates two-stage responses for 1★,
// single replies for 10% of positives, batches into `reviews` table.
//
// Run: node --env-file=.env.local scripts/039-seed-native-reviews.mjs [flags]
//
// Flags:
//   --dry             Print 15 diverse samples, no DB write
//   --limit=N         Only insert first N reviews (debugging)
//   --wipe            DELETE all source='native' rows before inserting
//   --seed=N          RNG seed (default 20260422, same as 038)
//
// Prereqs:
//   - Migration 034 applied (source/variant_name/external_id columns)
//   - Migration 037 applied (response_persona, response_first_reply_*, etc.)
//   - data/personas.json generated by scripts/038-generate-personas.mjs

import { createClient } from "@supabase/supabase-js"
import { readFileSync } from "node:fs"

const args = process.argv.slice(2)
const DRY = args.includes("--dry")
const WIPE = args.includes("--wipe")
const limitArg = args.find((a) => a.startsWith("--limit="))
const LIMIT = limitArg ? Number(limitArg.split("=")[1]) : Infinity
const seedArg = args.find((a) => a.startsWith("--seed="))
const SEED = seedArg ? Number(seedArg.split("=")[1]) : 20260422

// ---- deterministic RNG ----------------------------------------------------
function mulberry32(seed) {
  let a = seed >>> 0
  return () => {
    a = (a + 0x6d2b79f5) >>> 0
    let t = a
    t = Math.imul(t ^ (t >>> 15), t | 1)
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61)
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296
  }
}
const rand = mulberry32(SEED + 1) // offset so text choices differ from persona seed

function pick(arr) { return arr[Math.floor(rand() * arr.length)] }
function pickWeighted(entries) {
  const total = entries.reduce((s, [, w]) => s + w, 0)
  let r = rand() * total
  for (const [v, w] of entries) {
    r -= w
    if (r <= 0) return v
  }
  return entries[entries.length - 1][0]
}
function randInt(lo, hi) { return Math.floor(rand() * (hi - lo + 1)) + lo }

// ---- inputs ---------------------------------------------------------------
const plan = JSON.parse(readFileSync("./data/personas.json", "utf8"))
const vocab = JSON.parse(readFileSync("./data/style-vocab.json", "utf8"))

console.log(`plan: ${plan.reviews.length} reviews, ${plan.meta.stats.personasUsed} personas`)
if (!DRY) {
  console.log(`mode: ${WIPE ? "WIPE + INSERT" : "INSERT"}`)
  if (LIMIT !== Infinity) console.log(`limit: ${LIMIT}`)
}

// ---- text composition -----------------------------------------------------
// Pick a pool merging generic + product-category entries for the given
// rating/length/country. Category is derived from productId via the
// productCategory map in vocab. Ensures "bought the elite dma bundle"
// templates can't land on a Fortnite External card.
function pickPool(rating, lengthClass, country, productId) {
  const category = vocab.productCategory[productId] || "generic"

  const mergeGenCat = (bucket) => {
    if (!bucket) return []
    const gen = bucket.generic?.[country] || []
    const cat = bucket[category]?.[country] || []
    return [...gen, ...cat]
  }

  // 1★ is split by category (fortnite/spoofer/dma_cheat — no refund pool on
  // dma_bundle/dma_firmware because 038 blocks refunds on those products).
  if (rating === 1) {
    const bucket = vocab.short_1star
    const pool = bucket[category]?.[country] || bucket.fortnite[country] || []
    return pool.length ? pool : bucket.fortnite.us
  }

  const bucketShort = vocab[`short_${rating}star`]
  if (lengthClass === "short") {
    const merged = mergeGenCat(bucketShort)
    return merged.length ? merged : (bucketShort?.generic?.us || vocab.short_5star.generic.us)
  }

  // Medium/long — for 4★ fall through to 5★ pools (still positive in tone)
  const bucketLen = vocab[`${lengthClass}_5star`]
  const merged = mergeGenCat(bucketLen)
  if (merged.length) return merged

  // Ultimate fallback — generic short in this country
  return vocab.short_5star.generic[country] || vocab.short_5star.generic.us
}

function substitute(template) {
  return template
    .replaceAll("{staff}", pick(vocab.staffNames))
    .replaceAll("{weeks}", String(randInt(1, 8)))
    .replaceAll("{n}", String(randInt(2, 12)))
    .replaceAll("{hours}", String(randInt(20, 200)))
}

function applyTypos(text, country) {
  if (country !== "us") return text
  const subs = vocab.typoSubs
  const p = vocab.typoProbability ?? 0.08
  return text
    .split(/(\s+)/)
    .map((tok) => {
      if (/^\s+$/.test(tok)) return tok
      const lower = tok.toLowerCase()
      if (subs[lower] && rand() < p) return subs[lower]
      return tok
    })
    .join("")
}

function applyLowercase(text, country) {
  const p = vocab.lowercaseProbabilityByCountry[country] ?? 0.3
  if (rand() < p) return text.toLowerCase()
  return text
}

function maybeExclaim(text, rating) {
  // Exclaim boost only on 4★/5★ — low-star reviews with "!!!" read sarcastic
  if (rating < 4) return text
  if (rand() < vocab.exclamationBoostProbability) {
    return text.replace(/\.$/, "") + (rand() < 0.5 ? "!!" : "!!!")
  }
  return text
}

function maybeEmoji(text, rating) {
  // No emoji on low-star reviews — "refunded 🔥" reads unhinged
  if (rating < 4) return text
  if (rand() < vocab.emojiProbability) {
    // 4★ gets a softer subset (no 💯/🔥/👑 celebration icons)
    const pool = rating === 4 ? ["🙏", "❤️", "😏"] : vocab.emojis
    return text + " " + pick(pool)
  }
  return text
}

function maybeBracketTag(text) {
  if (rand() < vocab.bracketTagProbability) {
    return pick(vocab.bracketTags) + " " + text
  }
  return text
}

// Combinatorial short composition: {opener?} {main} {suffix?}
// Atoms × mains × suffixes × typos × emoji × case = >100k outputs per bucket,
// so 8k+ reviews stay unique without forced retries.
function composeShort(rating, country, productId) {
  const pool = pickPool(rating, "short", country, productId)
  const main = pick(pool)

  // Low-star refund text is self-contained ("refund", "bsod mobo unsupported").
  // Adding "recommend" or "no issues" as suffix creates contradictions. Keep
  // combinatorial variety only for 3★+.
  if (rating <= 2) return main

  const openers = vocab.atoms?.openers?.[country] ?? [""]
  const suffixes = vocab.atoms?.suffixes?.[country] ?? [""]
  const opener = pick(openers)
  let suffix = pick(suffixes)

  // Avoid echo: if suffix word matches opener, drop it (e.g. "bratan ok bratan")
  if (opener && suffix.trim().replace(/[,]/g, "").toLowerCase() === opener.toLowerCase()) {
    suffix = ""
  }
  // Avoid suffix word that already appears in main
  const suffixCore = suffix.trim().replace(/[,]/g, "").toLowerCase()
  if (suffixCore && main.toLowerCase().includes(suffixCore)) {
    suffix = ""
  }

  const prefix = opener ? opener + " " : ""
  return (prefix + main + suffix).trim()
}

function composeMedium(rating, country, productId) {
  const pool = pickPool(rating, "medium", country, productId)
  return substitute(pick(pool))
}

// Track generated texts (post-transform). Retry up to 8 times on collision;
// if still colliding, mutate by appending a secondary suffix or emoji to
// force uniqueness.
const seenTexts = new Set()

function applyAllTransforms(raw, country, rating) {
  let text = applyLowercase(raw, country)
  text = applyTypos(text, country)
  text = maybeExclaim(text, rating)
  text = maybeEmoji(text, rating)
  return text.trim().replace(/\s{2,}/g, " ")
}

function composeText(rating, lengthClass, country, productId) {
  let text = ""
  for (let attempt = 0; attempt < 8; attempt++) {
    const raw = lengthClass === "short"
      ? composeShort(rating, country, productId)
      : composeMedium(rating, country, productId)
    text = applyAllTransforms(raw, country, rating)
    if (!seenTexts.has(text)) {
      seenTexts.add(text)
      return text
    }
  }

  // Still colliding — mutate with a forced secondary suffix to separate it
  const suffixes = vocab.atoms?.suffixes?.[country] ?? [""]
  for (let i = 0; i < 10; i++) {
    const suffix = pick(suffixes.filter((s) => s !== ""))
    const forced = (text + (suffix || (rating >= 4 ? " " + pick(vocab.emojis) : " !"))).trim()
    if (!seenTexts.has(forced)) {
      seenTexts.add(forced)
      return forced
    }
  }

  // Final resort — append a unique counter suffix (should never trigger)
  const unique = text + " " + seenTexts.size
  seenTexts.add(unique)
  return unique
}

// ---- response composition -------------------------------------------------
function pickPersona(ujukPercent) {
  return rand() * 100 < ujukPercent ? "ujuk" : "vsx"
}

function buildRefundResponse(review) {
  const persona = pickPersona(70)
  const first = pick(vocab.firstReplyTemplates[persona])
  const hasCause = rand() < 0.55
  const category = vocab.productCategory[review.productId] || "fortnite"
  let updatePool
  if (hasCause) {
    // withCause is now category-split: { fortnite: [...], spoofer: [...] }
    const causeMap = vocab.updateResolvedTemplates[`${persona}_withCause`]
    updatePool = Array.isArray(causeMap) ? causeMap : (causeMap[category] || causeMap.fortnite)
  } else {
    updatePool = vocab.updateResolvedTemplates[`${persona}_simple`]
  }
  const update = pick(updatePool)

  const createdAtMs = new Date(review.createdAt).getTime()
  const firstReplyAtMs = createdAtMs + randInt(2, 8) * 3_600_000 + randInt(0, 59) * 60_000
  const updateAtMs = firstReplyAtMs + randInt(1, 3) * 86_400_000 + randInt(0, 12) * 3_600_000

  return {
    response_persona: persona,
    response_first_reply_text: first,
    response_first_reply_at: new Date(firstReplyAtMs).toISOString(),
    response_update_text: update,
    response_update_at: new Date(updateAtMs).toISOString(),
  }
}

function buildPositiveResponse(review) {
  const persona = pickPersona(50)
  const text = pick(vocab.positiveReplyShort[persona])
  const createdAtMs = new Date(review.createdAt).getTime()
  const replyAtMs = createdAtMs + randInt(1, 24) * 3_600_000 + randInt(0, 59) * 60_000
  return {
    response_persona: persona,
    response_first_reply_text: text,
    response_first_reply_at: new Date(replyAtMs).toISOString(),
    response_update_text: null,
    response_update_at: null,
  }
}

// ---- build all rows -------------------------------------------------------
function buildRow(review) {
  const text = composeText(review.rating, review.lengthClass, review.country, review.productId)

  let response = { response_persona: null, response_first_reply_text: null, response_first_reply_at: null, response_update_text: null, response_update_at: null }
  if (review.rating === 1) {
    response = buildRefundResponse(review)
  } else if (review.hasPositiveReply) {
    response = buildPositiveResponse(review)
  }

  return {
    text,
    rating: review.rating,
    product_id: review.productId,
    username: review.username,
    email: "",
    variant_name: review.variantName,
    source: "native",
    verified: true,
    is_auto: false,
    refunded: review.rating === 1,
    helpful: 0,
    needs_photo: review.hasPhoto,
    time_label: "", // API recomputes
    created_at: review.createdAt,
    team_response: null, // legacy field; new rows use response_* columns
    ...response,
  }
}

const rows = plan.reviews.slice(0, LIMIT).map(buildRow)

// ---- dry-run ? sample 15 diverse rows -------------------------------------
if (DRY) {
  console.log("\n── 15 DIVERSE SAMPLES ─────────────────────────")
  const samples = []
  const seen = new Set()
  // Pick one per (rating, country, lengthClass) combo when possible
  for (let i = 0; i < rows.length && samples.length < 15; i++) {
    const r = plan.reviews[i]
    const key = `${r.rating}-${r.country}-${r.lengthClass}`
    if (seen.has(key)) continue
    seen.add(key)
    samples.push({ plan: r, row: rows[i] })
  }
  // Fill leftover slots with random picks if distinct combos ran out
  while (samples.length < 15) {
    const idx = Math.floor(rand() * rows.length)
    samples.push({ plan: plan.reviews[idx], row: rows[idx] })
  }

  for (const [i, { plan: p, row }] of samples.entries()) {
    console.log("")
    console.log(`  #${i + 1}  ${row.rating}★  ${p.country.toUpperCase()}  ${p.lengthClass.padEnd(6)}  ${row.product_id}  (${row.variant_name})`)
    console.log(`       @${row.username}  ${row.created_at}`)
    console.log(`       ${JSON.stringify(row.text)}`)
    if (row.response_first_reply_text) {
      console.log(`       ↳ [${row.response_persona}] ${row.response_first_reply_text}`)
      if (row.response_update_text) {
        console.log(`       ↳ [update] ${row.response_update_text}`)
      }
    }
    if (row.needs_photo) console.log("       📷 needs_photo=true")
  }

  console.log("\n(--dry) skipping DB write")
  process.exit(0)
}

// ---- DB insert ------------------------------------------------------------
const SUPABASE_URL = process.env.NEXT_PUBLIC_SUPABASE_URL
const SUPABASE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY
if (!SUPABASE_URL || !SUPABASE_KEY) {
  console.error("Missing NEXT_PUBLIC_SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY. Run with --env-file=.env.local")
  process.exit(1)
}
const db = createClient(SUPABASE_URL, SUPABASE_KEY)

if (WIPE) {
  const { count: delCount, error: delErr } = await db
    .from("reviews")
    .delete({ count: "exact" })
    .eq("source", "native")
  if (delErr) {
    console.error("Wipe failed:", delErr.message)
    process.exit(1)
  }
  console.log(`✓ wiped ${delCount ?? 0} previous native rows`)
}

const BATCH = 200
let inserted = 0
const startedAt = Date.now()

for (let i = 0; i < rows.length; i += BATCH) {
  const chunk = rows.slice(i, i + BATCH)
  const { error } = await db.from("reviews").insert(chunk)
  if (error) {
    console.error(`batch ${i / BATCH + 1} failed:`, error.message)
    // show first row of failed batch for debugging
    console.error("first row:", JSON.stringify(chunk[0], null, 2).slice(0, 500))
    process.exit(1)
  }
  inserted += chunk.length
  const pct = ((inserted / rows.length) * 100).toFixed(1)
  const elapsed = ((Date.now() - startedAt) / 1000).toFixed(1)
  process.stdout.write(`  batch ${String(Math.ceil((i + BATCH) / BATCH)).padStart(3)}: +${chunk.length}  (${inserted}/${rows.length}  ${pct}%  ${elapsed}s)\n`)
}

console.log(`\n✓ inserted ${inserted} native reviews in ${((Date.now() - startedAt) / 1000).toFixed(1)}s`)
